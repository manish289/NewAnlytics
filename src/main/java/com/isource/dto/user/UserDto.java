package com.isource.dto.user;

import com.fasterxml.jackson.annotation.JsonProperty;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.ToString;

@Data
@AllArgsConstructor
@NoArgsConstructor
@ToString
public class UserDto {
	
	@JsonProperty("userstatus")
	private int userstatus;// integer,
	
	@JsonProperty("personname")
	private String personname;//  character varying
	
	@JsonProperty("userid")
	private int userid;// integer,
	
	@JsonProperty("companyserviceids")
	private String companyserviceids;//  character varying
	
	@JsonProperty("wordhighlight")
	private String wordhighlight;//  character varying

}
